public class Main {

    public static void main(String[] args) {
        //For
        // i değeri 1 olarak döngüye başlar ve
        // parantez içinde verdiğimiz koşulu sağlayana kadar devam eder.
        for (int i = 1; i <= 10; i++) {

            System.out.println(i);
        }
        System.out.println("döngü tamamlandı");
    }
}
